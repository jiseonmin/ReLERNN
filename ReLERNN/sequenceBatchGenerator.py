        self.maxLen = maxLen
        self.frameWidth = frameWidth
        self.center = center
        self.sortInds=sortInds
        self.ancVal = ancVal
        self.padVal = padVal
        self.derVal = derVal
        self.realLinePos = realLinePos
        self.posPadVal = posPadVal
        self.phase=phase
        self.seed=seed

        if self.seed:
            os.environ['PYTHONHASHSEED']=str(self.seed)
            random.seed(self.seed)
